     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $user = User::where('name', $request->name)->first();
        Booking::create([
            'name'=> $request->name,
            'user_id'=> $user->id,
            'movie_id'=> $request->movie_id,
        ]);
        return redirect()->route('index');
    }

